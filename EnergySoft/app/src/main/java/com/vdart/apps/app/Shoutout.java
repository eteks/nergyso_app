public class Shoutout {
    String shoutout_description;
    String shoutout_to;
    String shoutout_from;

    public String getShoutout_description() {
        return shoutout_description;
    }

    public void setShoutout_description(String shoutout_description) {
        this.shoutout_description = shoutout_description;
    }

    public String getShoutout_to() {
        return shoutout_to;
    }

    public void setShoutout_to(String shoutout_to) {
        this.shoutout_to = shoutout_to;
    }

    public String getShoutout_from() {
        return shoutout_from;
    }

    public void setShoutout_from(String shoutout_from) {
        this.shoutout_from = shoutout_from;
    }
}

